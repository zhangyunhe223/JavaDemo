package com.Chapter3.com;

public class Conver {

	public static void main(String[] args) {
		// 创建不同数值型的变量进行运算
		byte mybyte = 45;
		int myint = 14;
		float myfloat = 14.5F;
		char mychar = 10;
		double mydouble = 42.656565;
		System.out.println("mybyte + myfloat = " + (mybyte + myfloat));
		System.out.println("mybyte * myint = " + mybyte * myint);
		System.out.println("mybyte / mychar = " + mybyte / mychar);
		System.out.println("mydouble + mychar = " + (mydouble + mychar));
	}

}
