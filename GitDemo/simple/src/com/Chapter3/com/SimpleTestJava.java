package com.Chapter3.com;                                      // 定义包

public class SimpleTestJava {                                // 创建类
	
	static int ONE = 1;                                      // 定义类的成员变量

	public static void main(String[] args) {                 // 定义主方法
		
		String hello = "hello";                              // 定义局部变量
		
		System.out.println(ONE);                             // 输出成员变量的值
		 
		System.out.println(hello);                           // 输出局部变量的值
	}

}
